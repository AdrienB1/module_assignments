//Write a for loop that prints to the console the numbers 0 through 9.

for (i = 0; i <= 9; i++) {
    console.log(i)
}



//Write a for loop that prints to the console 9 through 0.

for (i = 9; i >= 0; i--) {
    console.log(i)
}



